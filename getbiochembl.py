def createdict(filein):
    with open(filein) as db:
        dtp=False
        uid=False
        mol=[]
        names=[]
        for lineraw in db:
            line=lineraw.strip()
            if line=='> <DTP names>':
                dtp=True
                continue
            if dtp==True and line=='':
                dtp=False
            if dtp==True:
                names.append(line.upper()) 
            if line=='> <Standard InChIKey>':
                uid=True
                continue
            if uid==True:
                id=line.replace('InChIKey=', '')
                uid=False
            if line=="$$$$":
                mol.append([id,names])
                names=[]
    return mol

